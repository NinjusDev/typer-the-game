let palabraAleatoria, time, score;

function randomWords() {
  const randomIndex = Math.floor(Math.random() * words.length);
  return words[randomIndex];
}

function addToDOM() {
  palabraAleatoria = randomWords();
  document.getElementById("randomWord").textContent = palabraAleatoria;
}

function actualizarTiempo() {
  time = 10;
  const timeSpan = document.getElementById("timeSpan");
  const countdown = setInterval(() => {
    time--;
    timeSpan.textContent = time + "s";
    if (time === 0) {
      clearInterval(countdown);
      gameOver();
    }
  }, 1000);
}

function updateScore() {
  score = score || 0; // Initialize score to 0 if it's undefined
  score++;
  document.getElementById("score").textContent = score;
}

function gameOver() {
  const endGameContainer = document.getElementById("end-game-container");
  const finalScore = score || 0; // Use 0 if score is undefined
  endGameContainer.innerHTML = `
      <h2>Se acab√≥ el tiempo!</h2>
      <p>Puntaje final: ${finalScore}</p>
      <button onclick="location.reload()">Jugar de nuevo</button>
    `;
}

document.getElementById("text").addEventListener("input", (e) => {
  const palabraIngresada = e.target.value.trim();
  if (palabraIngresada === palabraAleatoria) {
    time += 3;
    e.target.value = "";
    addToDOM();
    updateScore();
  }
});

addToDOM();
actualizarTiempo();
